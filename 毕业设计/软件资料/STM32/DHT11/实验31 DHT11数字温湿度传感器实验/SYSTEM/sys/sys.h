#include <iostream.h>
#include <vector>
#include <iterator>
#include <iomanip.h>
#include <stdio.h>

using namespace std;
//定义模板类和“指针”
vector<int> vec;
vector<int>::iterator it;

void Create(int n)
{
    for(int i=0; i<n; i++)
        vec.push_back(11*(i+2));
}


void Display()
{
    for(it=vec.begin(); it!=vec.end(); it++)
        cout<<setw(4)<<*it;
    cout<<endl;
}

void Insert(int x, int pos)
{
    it = vec.begin();
    it += pos;

    vec.insert(it, x);
}



void Delete(int pos)
{
    it = vec.begin();
    it += pos;
    vec.erase(it);
}

void Swap(int i, int j)
{
    swap(vec.at(i), vec.at(j));
}


void main()
{
    Create(7);
    Display();
    Swap(4, 0);
    Display();
    Delete(4);
    Display();
    Insert(66, 5);
    Display();
}










