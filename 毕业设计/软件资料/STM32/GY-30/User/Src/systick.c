#include "systick.h"

/********************************************************************
  静态全局变量说明：
        fac_us---us延时倍乘数
        fac_ms---ms延时倍乘数,在ucos下,代表每个节拍的ms数
*********************************************************************/
static u8  fac_us = 0; 
static u16 fac_ms = 0; 


/********************************************************************
* Function：   delay_init()
* Description: 初始化延迟函数;
* Input：  SYSCLK:系统时钟;SYSTICK的时钟固定为HCLK时钟的1/8
* Output： 无
* Return : 无
* Author:  XYD
* Others:  该函数设定系统滴答时钟的时钟源，确定us、ms的倍乘数
* date of completion:  2015-09-26
* date of last modify: 2015-09-26
*********************************************************************/
void Delay_Init(u8 SYSCLK)
{

    SysTick->CTRL &= ~ ( 1 << 2 ); /* SYSTICK使用外部时钟源 */
    fac_us = SYSCLK / 8;


    fac_ms = ( u16 ) fac_us * 1000; /* 非ucos下,代表每个ms需要的systick时钟数 */

}


/********************************************************************
*  Function   : delay_us ()
*  Description: 延时nus函数;
*  Input      : nus为要延时的us数.
*  Output     : 无
*  Author     : XYD
*  Others     : 
* date of completion:  2015-09-26
* date of last modify: 2015-09-26
*********************************************************************/
void Delay_us(u32 nus)
{
    u32 temp;
    SysTick->LOAD = nus * fac_us;               /* 时间加载 */
    SysTick->VAL = 0x00;                        /* 清空计数器 */
    SysTick->CTRL = 0x01 ;                      /* 开始倒数 */

    do
    {
        temp = SysTick->CTRL;
    }
	while ( ( temp & 0x01 ) && ! ( temp & ( 1 << 16 ) ) ); /* 等待时间到达 */
    SysTick->CTRL = 0x00;                        /* 关闭计数器 */
    SysTick->VAL = 0X00;                         /* 清空计数器 */
}


/********************************************************************
*  Function   : delay_ms()
*  Description: 延时nms函数;
*  Input      : nms为要延时的ms数.
*  Output     : 无
*  Author     : XYD
*  Others     :  1.注意nms的范围：
*                SysTick->LOAD为24位寄存器,
                 所以,最大延时为:nms<=0xffffff*8*1000/SYSCLK ；
                 SYSCLK单位为Hz,nms单位为ms，对72M条件下,nms<=1864
* date of completion:  2015-09-26
* date of last modify: 2015-09-26
*********************************************************************/
void Delay_ms (u16 nms)
{
    u32 temp;
    SysTick->LOAD = ( u32 ) nms * fac_ms;        /* 时间加载(SysTick->LOAD为24bit) */
    SysTick->VAL = 0x00;                         /* 清空计数器 */
    SysTick->CTRL = 0x01 ;                       /* 开始倒数 */

    do
    {
        temp = SysTick->CTRL;
    }
	while ( (temp & 0x01) && ! (temp & ( 1 << 16)) ); /* 等待时间到达 */

    SysTick->CTRL = 0x00;                        /* 关闭计数器 */
    SysTick->VAL = 0X00;                         /* 清空计数器 */
}








